const PPS_I18N = (() => {
  const originalText = new WeakMap();
  const originalPlaceholder = new WeakMap();

  function getLang(){
    return localStorage.getItem("pps_lang") || "en";
  }

  function setLang(lang){
    const next = lang === "fr" || lang === "ko" ? lang : "en";
    const current = getLang();
    localStorage.setItem("pps_lang", next);
    applyTranslations(next);
    if(current !== next){
      window.dispatchEvent(new CustomEvent("pps:lang", { detail:{ lang: next } }));
      window.location.reload();
    }
  }

  function t(key, lang){
    const langKey = lang || getLang();
    const set = PPS_TRANSLATIONS[langKey] || PPS_TRANSLATIONS.en;
    return set?.[key] || PPS_TRANSLATIONS.en?.[key] || "";
  }

  function interpolate(text){
    const year = new Date().getFullYear();
    return text.replace(/\{\{year\}\}/g, String(year));
  }

  function applyTranslations(lang){
    const current = lang || getLang();
    document.documentElement.lang = current === "fr" ? "fr" : (current === "ko" ? "ko" : "en");

    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
      if(!originalText.has(el)) originalText.set(el, el.textContent);
      const key = el.getAttribute("data-i18n");
      const value = t(key, current);
      if(value){
        el.textContent = interpolate(value);
      }else{
        el.textContent = originalText.get(el);
      }
    });

    const placeholders = document.querySelectorAll("[data-i18n-placeholder]");
    placeholders.forEach(el => {
      if(!originalPlaceholder.has(el)) originalPlaceholder.set(el, el.getAttribute("placeholder") || "");
      const key = el.getAttribute("data-i18n-placeholder");
      const value = t(key, current);
      if(value){
        el.setAttribute("placeholder", value);
      }else{
        el.setAttribute("placeholder", originalPlaceholder.get(el));
      }
    });

    const titles = document.querySelectorAll("[data-i18n-title]");
    titles.forEach(el => {
      const key = el.getAttribute("data-i18n-title");
      const value = t(key, current);
      if(value) el.setAttribute("title", value);
    });
  }

  return { getLang, setLang, t, applyTranslations };
})();

window.PPS_I18N = PPS_I18N;

window.addEventListener("DOMContentLoaded", () => {
  PPS_I18N.applyTranslations();
});