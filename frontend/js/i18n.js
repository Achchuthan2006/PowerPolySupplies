  function autoTranslate(text, lang){
    const langKey = normalizeLang(lang || getLang());
    if(langKey === "es") return autoTranslateEs(text);
    return text;
  }

  function t(key, lang){
    const langKey = normalizeLang(lang || getLang());
    const set = PPS_TRANSLATIONS[langKey] || PPS_TRANSLATIONS.en;
    const base = set?.[key] || "";
    let value = base || PPS_TRANSLATIONS.en?.[key] || "";
    if(langKey === "es" && !base && PPS_TRANSLATIONS.en?.[key]){
      value = autoTranslateEs(PPS_TRANSLATIONS.en[key]);
    }
    if(langKey === "ta" && value){
      const hasTamil = /[\u0B80-\u0BFF]/.test(value);
      const hasReplacement = /\uFFFD/.test(value);
      if((!hasTamil || hasReplacement) && PPS_TRANSLATIONS.en?.[key]){
        value = PPS_TRANSLATIONS.en[key];
      }
    }
    return value;
  }

  function interpolate(text){
    const year = new Date().getFullYear();
    return text.replace(/\{\{year\}\}/g, String(year));
  }

  function applyTranslations(lang){
    const current = normalizeLang(lang || getLang());
    document.documentElement.lang = current;

    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
      if(!originalText.has(el)) originalText.set(el, el.textContent);
      const key = el.getAttribute("data-i18n");
      const value = t(key, current);
      if(value){
        el.textContent = interpolate(value);
      }else{
        el.textContent = originalText.get(el);
      }
    });

    const placeholders = document.querySelectorAll("[data-i18n-placeholder]");
    placeholders.forEach(el => {
      if(!originalPlaceholder.has(el)) originalPlaceholder.set(el, el.getAttribute("placeholder") || "");
      const key = el.getAttribute("data-i18n-placeholder");
      const value = t(key, current);
      if(value){
        el.setAttribute("placeholder", value);
      }else{
        el.setAttribute("placeholder", originalPlaceholder.get(el));
      }
    });

    const titles = document.querySelectorAll("[data-i18n-title]");
    titles.forEach(el => {
      const key = el.getAttribute("data-i18n-title");
      const value = t(key, current);
      if(value) el.setAttribute("title", value);
    });

    if(current === "es"){
      autoTranslateDom();
    }
  }

  function shouldSkipAutoTranslate(node){
    if(!node || !node.parentElement) return true;
    const parent = node.parentElement;
    if(parent.closest("script,style,code,pre,textarea,input,select,option")) return true;
    if(parent.closest("[data-i18n],[data-i18n-placeholder],[data-i18n-title],[data-no-translate],.no-translate")) return true;
    return false;
  }

  function isSkippableText(text){
    const raw = text || "";
    const trimmed = raw.trim();
    if(!trimmed) return true;
    if(/^[\s\-_|,.:;!?()\[\]{}\/\\]+$/.test(trimmed)) return true;
    if(/^[0-9\s$.,%()+\-]+$/.test(trimmed)) return true;
    if(trimmed === "Power Poly Supplies") return true;
    return false;
  }

  function translateTextNode(node){
    if(shouldSkipAutoTranslate(node)) return;
    const original = originalAutoText.get(node) ?? node.nodeValue;
    if(originalAutoText.has(node) === false) originalAutoText.set(node, original);
    if(isSkippableText(original)) return;
    const translated = autoTranslateEs(original);
    if(translated && translated !== node.nodeValue) node.nodeValue = translated;
  }

  function autoTranslateDom(){
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
    let node = walker.nextNode();
    while(node){
      translateTextNode(node);
      node = walker.nextNode();
    }
  }

  let autoTranslateObserver = null;
  function ensureAutoTranslateObserver(){
    if(autoTranslateObserver) return;
    autoTranslateObserver = new MutationObserver((mutations)=>{
      if(getLang() !== "es") return;
      mutations.forEach((m)=>{
        m.addedNodes.forEach((node)=>{
          if(node.nodeType === Node.TEXT_NODE){
            translateTextNode(node);
          }else if(node.nodeType === Node.ELEMENT_NODE){
            const walker = document.createTreeWalker(node, NodeFilter.SHOW_TEXT, null);
            let child = walker.nextNode();
            while(child){
              translateTextNode(child);
              child = walker.nextNode();
            }
          }
        });
      });
    });
    if(document.body){
      autoTranslateObserver.observe(document.body, { childList:true, subtree:true });
    }
  }

  ensureAutoTranslateObserver();

  return { getLang, setLang, t, applyTranslations, autoTranslate };
