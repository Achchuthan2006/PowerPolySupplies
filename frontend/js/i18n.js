  const defaultLangs = ["en", "fr", "es", "ko", "hi", "ta"];
  const getAllowedLangs = () => {
    const raw = document.documentElement?.dataset?.langOptions;
    if(!raw) return defaultLangs;
    const list = raw.split(",").map(s => s.trim()).filter(Boolean);
    if(!list.includes("en")) list.unshift("en");
    return list;
  };
  const normalizeLang = (lang) => {
    const allowed = getAllowedLangs();
    return allowed.includes(lang) ? lang : "en";
  };

  function readCookie(name){
    const match = document.cookie.match(new RegExp(`(?:^|; )${name}=([^;]*)`));
    return match ? decodeURIComponent(match[1]) : "";
  }

  function writeCookie(name, value){
    const maxAge = 60 * 60 * 24 * 365;
    document.cookie = `${name}=${encodeURIComponent(value)}; max-age=${maxAge}; path=/; samesite=lax`;
  }

  function getLang(){
    try{
      const fromQuery = new URLSearchParams(window.location.search).get("lang");
      return normalizeLang(fromQuery || localStorage.getItem("pps_lang") || readCookie("pps_lang") || "en");
    }catch(err){
      const fromQuery = new URLSearchParams(window.location.search).get("lang");
      return normalizeLang(fromQuery || readCookie("pps_lang") || "en");
    }
  }

